n1 = input("Digite o primeiro número: ")
n2 = input("Digite o segundo número: ")

soma = float(n1) + float(n2)
subtracao = float(n1) - float(n2)
multiplicacao = float(n1) * float(n2)
divisao = float(n1) / float(n2)

print("A soma é: " + str(soma))
print("A subtração é: " + str(subtracao))
print("A multiplicação é: " + str(multiplicacao))
print("A divisão é: " + str(divisao))